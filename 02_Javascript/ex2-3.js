var value = 0;

function reset() {
    value = 0;
    alert(value);
}

function add() {
    value += 1;
    alert(value);
}

function subtract() {
    value -= 1;
    alert(value);
}


function multiple() {
    value *= 2;
    alert(value);
}

function divide() {
    value /= 2;
    alert(value);
}

